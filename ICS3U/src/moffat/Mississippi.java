public class Mississippi {

	/*
	 * This Method is used to print the letter "S" using
	 * all capital letter "S"s
	 */
	public static void sLetter()
	{
		System.out.println("SSSSSSS");
		System.out.println("S      ");
		System.out.println("S      ");
		System.out.println("SSSSSSS");
		System.out.println("      S");
		System.out.println("      S");
		System.out.println("SSSSSSS");
	}
	
	/*
	 * This method is used to print the letter "M" using
	 * all capital letter "M"s
	 */
	public static void mLetter()
	{
		System.out.println("M       M");
		System.out.println("MM     MM ");
		System.out.println("M M   M M");
		System.out.println("M   M   M");
		System.out.println("M       M");
		System.out.println("M       M");
		System.out.println("M       M");
		
	}
	
	/*
	 * This method is used to print the letter "P" using
	 * all capital letter "P"s
	 */
	public static void pLetter()
	{
		System.out.println("PPPPPPP");
		System.out.println("P     P");
		System.out.println("P     P");
		System.out.println("PPPPPPP");
		System.out.println("P");
		System.out.println("P");
		System.out.println("P");
	}
	
	/*
	 * This method is used to print the letter "I" using
	 * all capital letter "I"s
	 */
	public static void iLetter()
	{
		System.out.println("IIIIIII");
		System.out.println("   I  ");
		System.out.println("   I  ");
		System.out.println("   I  ");
		System.out.println("   I  ");
		System.out.println("   I  ");
		System.out.println("IIIIIII");
	}
	
	
	
	public static void main(String[] args) {
		// TODO Auto-generated method stub
		
		//Calling methods and spacing out letters
		mLetter();
		System.out.println("");
		iLetter();
		System.out.println("");
		sLetter();
		System.out.println("");
		sLetter();
		System.out.println("");
		iLetter();
		System.out.println("");
		sLetter();
		System.out.println("");
		sLetter();
		System.out.println("");
		iLetter();
		System.out.println("");
		pLetter();
		System.out.println("");
		pLetter();
		System.out.println("");
		iLetter();

	}

}
